## AWS CodePipeline
## AWS CodePipeline es un servicio de entrega continua, completamente
## administrado, que permite automatizar el proceso de lanzamiento, para lograr
## actualizaciones de infraestructura y aplicaciones, rápidas y fiables
## CodePipeline automatiza las fases de compilación, pruebas e implementación del
## proceso de lanzamiento, cada vez que se realiza una modificación en el código
## en función del modelo de lanzamiento que se defina
## Esto permite entregar características y actualizaciones de forma rápida y fiable
## Se integra fácilmente con servicios de terceros como GitHub

